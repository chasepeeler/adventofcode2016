	for($y=0;$y<count($messages[$x]);$y++){
		$transpose[$y][$x] = $messages[$x][$y];
	}
}

//loop through each row
foreach($transpose as $t){
	//array_count_Values gives us our letters as keys, and frequency as the values
	$counts = array_count_values($t);

	//since we want the lease common letter, regular sort
	asort($counts);
	//reset the pointer, just in case
	reset($counts);
	
	//get the key at the top
	$output .= key($counts);
}

